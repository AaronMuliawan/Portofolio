package com.example.guidechessrush;

public class Game {
    private String name;
    private String img;
    private String desc;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    String getImg() {
        return img;
    }

    void setImg(String img) {
        this.img = img;
    }

    String getDesc() {
        return desc;
    }

    void setDesc(String desc) {
        this.desc = desc;
    }
}
